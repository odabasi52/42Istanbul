#include <stdlib.h>

unsigned int	ft_strlen(char *str)
{
	unsigned int	i;

	i = 0;
	while (str[i])
		i++;
	return (i);
}

char	*ft_strdup(char *src)
{
	unsigned int		len;
	int					i;
	char				*ptr;

	len = ft_strlen(src);
	ptr = (char *)malloc((len + 1) * 1);
	if (!ptr)
		return ((void *)0);
	i = 0;
	while (src[i])
	{
		ptr[i] = src[i];
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
