int	ft_any(char **tab, int (*f)(char*))
{
	int		i;
	int		retval;

	i = 0;
	while (tab[i] != 0)
	{
		retval = f(tab[i]);
		if (retval != 0)
			return (1);
		i++;
	}
	return (0);
}
